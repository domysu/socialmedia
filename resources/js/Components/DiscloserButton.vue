});
const isOpen = ref(false)

function openModal() {
  isOpen.value = true
}

function onUpdate() {

  const form = useForm({
    id: props.post.id,
    body: props.post.body
  
  })
  
  form.put(route('post.update', props.post))
  isOpen.value = false;

}
function onDelete() {
  router.delete(route("post.delete", props.post));
}
